#ifndef BITCODE_H
#define BITCODE_H


#include <stdint.h>


#define BITCODE(name) static const int8_t name

BITCODE(BYTE) = 0044;
BITCODE(REG) = 0100;

BITCODE(INC) = 0151;
BITCODE(MOV) = 0075;
BITCODE(ADD) = 0053;
BITCODE(SUB) = 0055;
BITCODE(MUL) = 0052;
BITCODE(SQ) = 0062;

BITCODE(REGA) = 0101;
BITCODE(REGB) = 0102;
BITCODE(REGC) = 0103;
BITCODE(REGD) = 0104;

BITCODE(DEBUG) = 0140;


#endif
